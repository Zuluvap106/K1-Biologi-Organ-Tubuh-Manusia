function showContent(topic) {
  if (content[topic]) {
    document.getElementById("content").innerHTML = `
                    <div class="content-box border border-gray-300 p-4 rounded-lg shadow-lg bg-white">
                        <img src="${content[topic].img}" alt="${topic}" class="rounded-lg shadow-lg w-full max-w-xs mx-auto">
                        <p class="text-gray-700 mt-4 text-center">${content[topic].text}</p>
                        <button onclick="showQuestion('${topic}')" class="btn bg-purple-500 hover:bg-purple-700 text-white px-4 py-2 rounded-lg mt-4">Lihat Kuis</button>
                    </div>
                `;
  } else {
    document.getElementById(
      "content"
    ).innerHTML = `<p class='text-red-500 text-center'>Materi belum tersedia!</p>`;
  }
}

function showQuestion(organ) {
  if (questions[organ]) {
    const overlay = document.getElementById("overlay");
    const questionBox = document.getElementById("question-box");
    const answerButtons = document.getElementById("answer-buttons");
    const questionText = document.getElementById("question-text");

    if (overlay && questionBox && answerButtons && questionText) {
      overlay.classList.remove("hidden");
      questionBox.classList.remove("hidden");
      questionText.innerText = questions[organ].question;
      answerButtons.innerHTML = "";

      questions[organ].answers.forEach((answer) => {
        const button = document.createElement("button");
        button.innerText = answer;
        button.classList.add(
          "answer-btn",
          "bg-blue-500",
          "text-white",
          "px-4",
          "py-2",
          "rounded-lg",
          "hover:bg-blue-700",
          "transition"
        );
        button.onclick = function () {
          checkAnswer(answer, questions[organ].correct);
        };
        answerButtons.appendChild(button);
      });
      document.getElementById("feedback").innerHTML = "";
    } else {
      console.error("Required elements for the quiz are missing in the HTML.");
    }
  } else {
    console.error("Question not found for organ:", organ);
  }
}

function checkAnswer(selected, correct) {
  const feedback = document.getElementById("feedback");
  if (feedback) {
    feedback.innerHTML = "";
    if (selected === correct) {
      feedback.innerHTML = "✅ <b class='text-green-500'>Jawaban benar!</b>";
    } else {
      feedback.innerHTML =
        "❌ <b class='text-red-500'>Jawaban salah, coba lagi.</b>";
    }
  } else {
    console.error("Feedback element is missing in the HTML.");
  }
}

function hideQuestion() {
  const overlay = document.getElementById("overlay");
  const questionBox = document.getElementById("question-box");

  if (overlay && questionBox) {
    overlay.classList.add("hidden");
    questionBox.classList.add("hidden");
  } else {
    console.error("Overlay or question box element is missing in the HTML.");
  }
}

document.getElementById("menu-toggle").addEventListener("click", function () {
  const menu = document.getElementById("menu");
  if (menu) {
    menu.classList.toggle("show");
  }
});

window.addEventListener("resize", function () {
  const menu = document.getElementById("menu");
  if (window.innerWidth >= 769) {
    menu.classList.remove("hidden");
    menu.classList.remove("show");
  } else {
    menu.classList.add("hidden");
  }
});

function createFallingHearts() {
  const heart = document.createElement("div");
  heart.classList.add("falling-heart");
  heart.innerHTML = "❤️"; // Bisa diganti dengan 💖 atau 💗

  // Set posisi jatuh secara acak
  heart.style.left = Math.random() * window.innerWidth + "px";
  heart.style.animationDuration = Math.random() * 3 + 2 + "s"; // Antara 2-5 detik

  document.body.appendChild(heart);

  // Hapus elemen setelah selesai animasi
  setTimeout(() => {
    heart.remove();
  }, 5000); // Hapus setelah 5 detik
}

// Jalankan fungsi setiap 500 milidetik
setInterval(createFallingHearts, 500);

